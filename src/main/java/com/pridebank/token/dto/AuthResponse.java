@Data
@Builder
@NoArgsConstructor
@AllArgsConstructor
@JsonInclude(JsonInclude.Include.NON_NULL)
public class AuthResponse {

    private String authToken;
    private String tokenType;
    private long expiresIn;
    private String message;
}